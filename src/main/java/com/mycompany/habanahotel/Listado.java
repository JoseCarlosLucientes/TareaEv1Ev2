    /**
     * Cambia el estilo de la tabla, alternando entre diferentes fuentes y
     * colores. Cada llamada alterna al siguiente estilo en el ciclo.
     */
    public void cambiarEstiloTabla() {
        JTableHeader th = tblListado.getTableHeader();
        Font fuente;
        Color headerColor;
        Color rowColor;

        // Alternar entre diferentes estilos
        switch (styleCounter) {
            case 0:
                fuente = new Font("Arial Rounded MT Bold", Font.BOLD, 14);
                headerColor = Color.LIGHT_GRAY;
                rowColor = Color.WHITE;
                break;
            case 1:
                fuente = new Font("Verdana", Font.ITALIC, 12);
                headerColor = Color.DARK_GRAY;
                rowColor = Color.LIGHT_GRAY;
                break;
            case 2:
                fuente = new Font("SansSerif", Font.PLAIN, 16);
                headerColor = Color.BLUE;
                rowColor = Color.CYAN;
                break;
            default:
                fuente = new Font("Arial Rounded MT Bold", Font.BOLD, 14);
                headerColor = Color.LIGHT_GRAY;
                rowColor = Color.WHITE;
                styleCounter = 0; // Resetear contador
                break;
        }

        // Aplicar el estilo seleccionado
        th.setFont(fuente);
        th.setBackground(headerColor);
        tblListado.setBackground(rowColor);

        // Incrementar el contador
        styleCounter++;
    }


