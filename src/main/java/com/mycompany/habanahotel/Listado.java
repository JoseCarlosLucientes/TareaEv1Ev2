    /**
     * Carga las reservas desde la matriz de datos en la tabla. Recorre la
     * matriz hasta encontrar una celda vacía.
     */
    public void cargarReservas() {

        boolean seguirBuscando = true;
        int i = 0;

        //recorrer la matriz para ver las reservas en la tabla
        while (seguirBuscando) {
            if (matriz[i][CONST_NOMBRE] == null) {
                seguirBuscando = false;
            } else {
                tblListado.setValueAt(matriz[i][CONST_NOMBRE], i, CONST_NOMBRE);
                tblListado.setValueAt(matriz[i][CONST_TELEFONO], i, CONST_TELEFONO);
                tblListado.setValueAt(matriz[i][CONST_TIPOEVENTO], i, CONST_TIPOEVENTO);
                tblListado.setValueAt(matriz[i][CONST_JORNADAS], i, CONST_JORNADAS);
                tblListado.setValueAt(matriz[i][CONST_HABITACIONES], i, CONST_HABITACIONES);
                tblListado.setValueAt(matriz[i][CONST_FECHA], i, CONST_FECHA);
                tblListado.setValueAt(matriz[i][CONST_COCINA], i, CONST_COCINA);
                tblListado.setValueAt(matriz[i][CONST_PERSONAS], i, CONST_PERSONAS);
                i = i + 1;
            }
        }
    }

